// You can create a function with a parameter. Then add return parameter with an additional value. When the function is called with a variable it will return the variable along with the value.

// Example
function minusSeven(num) {
  return num - 7;
}

// Only change code below this line
function timesFive(num) {
  return num * 5;
}


