// For loops are comprised of three expressions: initialization, condition, final-expression, each separated by a semicolon. For loops will run for the specified number of times. Values can be pushed to an array by setting a value in initialization, setting the condition in condition, and incrementing in final-expression.  

// Example
var ourArray = [];

for (var i = 0; i < 5; i++) {
  ourArray.push(i);
}

