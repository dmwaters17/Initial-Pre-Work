// Variables within functions that act as placeholders are parameters. When the parameters hold actual values they are called arguments. You can create a function with variables and then call it with any assortment of values. 

// Example
function ourFunctionWithArgs(a, b) {
  console.log(a - b);
}
ourFunctionWithArgs(10, 5); // Outputs 5

